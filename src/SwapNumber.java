public class SwapNumber {
    public static void main(String[] args) {
        int a =20;
        int b=30;

        System.out.println("Value of a: "+a);
        System.out.println("Value of a: "+b);


        int temp;
        temp=a;
        a=b;
        b=temp;

        System.out.println("Value of a: "+a);
        System.out.println("Value of a: "+b);


    }
}

public class VowelOrConsonant {
    public static void main(String[] args) {
        char ch='w';
        if (ch=='a'||ch=='e'||ch=='i'||ch=='0'|| ch=='u')
        {
            System.out.println(ch+" is vowel");
        }
        else {
            System.out.println(ch+" is consonant");
        }
    }
}
